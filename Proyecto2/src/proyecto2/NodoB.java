public class NodoB {
    
    int ISBN;
    String nombre;
    String autor;
    String editorial;
    int año;
    int edicion;
    String categoria;
    String idioma;
    int carnet;
    
    
    public NodoB(int ISBN, String nombre, String autor, String editorial, int año, int edicion, String categoria, String idioma, int carnet) {
        this.ISBN = ISBN;
        this.nombre = nombre;
        this.autor = autor;
        this.editorial = editorial;
        this.año = año;
        this.edicion = edicion;
        this.categoria = categoria;
        this.idioma = idioma;
        this.carnet = carnet;
        
    }
    
    
    
}
